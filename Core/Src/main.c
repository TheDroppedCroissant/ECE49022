void Thermal(void){
    volatile float r_t = desired_temp;
    float y_t, u_t, diff, P, I;
    float MAX_LIMIT = 10;
    float MIN_LIMIT = -10;
	float K_p = 2;
    float K_i = 0.5;
    float diff_sum = 0;
    float last_diff = 0;

    while (1){
        y_t = get_sensor_data();
        diff = r_t - y_t;
        diff_sum = diff + last_diff;
        P = P + K_p * diff;
        I = K_i * diff_sum;
        if(I > MAX_LIMIT) I = MAX_LIMIT;
        else if(I < MIN_LIMIT) I = MIN_LIMIT;
        u_t = P + I;
        if(u_t > MAX_LIMIT) u_t = MAX_LIMIT;
        else if(u_t < MIN_LIMIT) u_t = MIN_LIMIT;
        set_Vout(u_t);
    }; //loops until process is killed by the controller

    return;
}

float get_sensor_data(void){
	double temp = 0;
	uint8_t buf[8][2];
	float temp_vals[8];
	int temp_hold;
	for(uint8_t i = 0x48; i < 0x50; i += 1){
		buf[i][1] = 0x00;
		if(HAL_I2C_Master_Transmit(&hi2c1, i, buf[i], 1, 5000) == HAL_OK ){
			HAL_I2C_Master_Receive(&hi2c1, i, buf[i], 2, 5000);
		}
		temp_hold = (int) buf[i][0];
		temp_hold <<= 4;
		temp_vals[i] = (float) (temp_hold | (buf[i][1] & 0xF0));
		temp_vals[i] = temp_vals[i] > 0 ? temp_vals[i] / 16: (temp_vals[i] - 4096) / 16;
		temp += temp_vals[i];
	}
	return (temp / 8);
}

